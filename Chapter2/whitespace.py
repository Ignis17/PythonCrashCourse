# Stripping Whitespace

fav_lang = "  python  "
print(fav_lang)

# Strips whitespaces to the right end of the string.
print(fav_lang.rstrip())

# Strips whitespaces to the left end of the string.
print(fav_lang.lstrip())

# Strips whitespaces from both left and right end of the string
print(fav_lang.strip())
